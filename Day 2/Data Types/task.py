#Pulling out a particular element from a string is called subscripting
print("Hello"[0])
#It will print 'H'

#String
print("123" + "456") #Concatenation

#Integer = Whole number
print(123 + 456)

#Large Integers
print(123_456_789)#Easier to read since you cant use commas

#Float = floating point number eg 3.14 etc. As it has a decimal place
print(3.14)

#Boolean = #only either True or False
print(True)
print(False)

#This shows us the data type it takes any data types and tells us
print(type("Hello")) #class 'str'
print(type(314))#class 'int'
print(type(3.14))#class 'float'
print(type(False))#class 'bool'

